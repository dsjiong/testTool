    # 要查找的 name
    def find_hierarchy_by_name(self, name):

        result_hierarchy = []

        # 查找匹配的记录，next()返回迭代器的下一个项目，默认值不给抛出StopIteration 异常
        current_record = next((record for record in self.data if record['name'] == name), None)

        if current_record:
            # 获取当前记录的code
            result_hierarchy.append({"code": current_record['code'], "name": current_record['name']})

            # 递归查找上级记录，直到1级
            parent_id = current_record['parent_id']
            while parent_id != 0:
                parent_record = next(item for item in self.data if item['group_id'] == parent_id)
                # list.insert(index，object)函数，将object插入列表index位置
                result_hierarchy.insert(0, {"code": parent_record['code'], "name": parent_record['name']})
                parent_id = parent_record['parent_id']
            return result_hierarchy
        return None

